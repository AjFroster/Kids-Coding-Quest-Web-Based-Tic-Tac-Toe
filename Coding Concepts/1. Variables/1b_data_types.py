# Integer
a = 10

# Float
b = 3.14

# String
c = "Hello, World!"

# Boolean
d = True

# List
e = [1, 2, 3, "apple", "banana"]

# Tuple
f_tuple = (1, 2, 3, "orange")  # Renamed to avoid confusion with f-string

# Dictionary
g = {"name": "Alice", "age": 30}

# Set
h = {1, 2, 3, 4, 5}

# Printing all variables using f-strings
print(f"Integer: {a}")
print(f"Float: {b}")
print(f"String: {c}")
print(f"Boolean: {d}")
print(f"List: {e}")
print(f"Tuple: {f_tuple}")
print(f"Dictionary: {g}")
print(f"Set: {h}")
